x = 5
print(x, type(x)) # int

x = 5.5
print(x, type(x)) # float

x = "Hello"
print(x, type(x)) # str

x = True
print(x, type(x)) # bool

x = ["apple", "banana"]
print(x, type(x)) # list

x = ("apple", "banana")
print(x, type(x)) # tuple

x = {"name": "John", "age": 20}
print(x, type(x)) # dict

x = {"apple", "banana"}
print(x, type(x)) # set

x = None
print(x, type(x)) # NoneType
