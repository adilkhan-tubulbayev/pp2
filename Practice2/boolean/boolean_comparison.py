# Boolean Comparison Results - W3Schools Examples
# Comparing values returns True or False

# Example 1: Basic boolean comparisons
print(10 > 9)   # True
print(10 == 9)  # False
print(10 < 9)   # False

# Example 2: Using comparison in if statement
a = 200
b = 33
if b > a:
    print("b is greater than a")
else:
    print("b is not greater than a")

# Example 3: Comparison operators
x = 5
y = 10
print(x == y)  # False - equal to
print(x != y)  # True  - not equal to
print(x < y)   # True  - less than
print(x > y)   # False - greater than
print(x <= y)  # True  - less than or equal to
print(x >= y)  # False - greater than or equal to
