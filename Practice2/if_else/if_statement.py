# If Statement - W3Schools Examples
# The if keyword is used to execute code if a condition is True

# Example 1: Basic if statement
a = 33
b = 200
if b > a:
    print("b is greater than a")

# Example 2: Checking positive numbers
number = 15
if number > 0:
    print("The number is positive")

# Example 3: Multiple statements in if block
age = 20
if age >= 18:
    print("You are an adult")
    print("You can vote")
    print("You have full legal rights")

# Example 4: Boolean variable in if statement
is_logged_in = True
if is_logged_in:
    print("Welcome back!")
